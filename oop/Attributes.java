package oop;

public class Attributes {
	
	
	 private String first;   // Encapsulation
	 private String second;
	 private String third;
	
	
	public String getFirst()  // Encapsulation
	{
		return first;
	}
	public  void setFirst(String first)  // Encapsulation
	{
		this.first = first;
	}
	public String getSecond()  // Encapsulation
	{
		return second;
	}
	public void setSecond(String second)  // Encapsulation
	{
		this.second = second;
	}
	public String getThird()  // Encapsulation
	{
		return third;
	}
	public void setThird(String third)  // Encapsulation
	{
		this.third = third;
	}
	
	



}
